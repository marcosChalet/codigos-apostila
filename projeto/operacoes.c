#include "operacoes.h"

double somar(const double n1, const double n2) { return n1 + n2; }

double subtrair(const double n1, const double n2) { return n1 - n2; }

double multiplicar(const double n1, const double n2) { return n1 * n2; }

double dividir(const double n1, const double n2) { return n1 / n2; }
